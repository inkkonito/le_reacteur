// Exercice 1 : Vérifier si tous les éléments sont positifs
// Objectif : Utiliser every() pour vérifier si tous les éléments d'un tableau sont des nombres positifs.

// Énoncé :

// Tu as un tableau de nombres :

// const nombres = [2, 5, 8, 10, 15];
// Utilise every() pour vérifier si tous les nombres dans le tableau sont supérieurs à 0.
// Affiche le résultat (true ou false).

const nombres = [2, 5, 8, 10, 15];
nombresPositive = nombres.every(elem => elem > 0)
console.log(nombresPositive)

